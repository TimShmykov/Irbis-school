package Zoo;

public class Dog extends Animal {

    public Dog(String species, String name, String colour, int age, int tail, boolean voice) {
        this.species = species;
        this.name = name;
        this.colour = colour;
        this.age = age;
        this.foots = 4;
        this.wings = 0;
        this.tail = tail;
        this.voice = voice;
        this.predator = true;

    }
}
