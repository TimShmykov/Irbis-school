package Zoo;

public class Cat extends Animal {

    public Cat(String species, String name, String colour, int age, boolean voice) {
        this.species = species;
        this.name = name;
        this.colour = colour;
        this.age = age;
        this.voice = voice;
        this.foots = 4;
        this.wings = 0;
        this.tail = 1;
        this.predator = true;

    }

}
